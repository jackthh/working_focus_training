package com.example.working_focus.Home;

public class SoundListItem {
    private int mImage;
    private String mTittle;

    public SoundListItem(int image, String tittle) {
        mImage = image;
        mTittle = tittle;
    }

    public int getImage() {
        return mImage;
    }

    public String getTittle() {
        return mTittle;
    }
}
